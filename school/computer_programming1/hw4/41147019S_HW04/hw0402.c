int main() {
    int board[16][30];
    int row[480];
    int col[480];
    for (int i = 0; i < 16; i++) {
        for (int j = 0; j < 30; j++) {
            scanf("%d", &board[i][j]);
        }
    }
    find_good_cells(board, row, col);
    printf("row: [");
    for (int i = 0; i < 480; ++i) {
        printf("%3d", row[i]);
    }
    printf(" ]\ncol: [");
    for (int i = 0; i < 480; ++i) {
        if (row[i] != -1 && col[i] != -1) {
            printf("%3d", col[i]);
        }
    }
    printf(" ]\n");
    return 0;
}